// The IBM VGA (mode 13h) default DAC palette every VGA BIOS ships: the 16
// CGA/EGA colors, a 16-step gray ramp, then three brightness tiers each holding
// a 24-hue wheel at three saturation levels (verified ramp-for-ramp against
// DOSBox's vga_palette table). 6-bit DAC values expand to 8-bit by bit
// replication. Deduplicated: the gray ramp's endpoints repeat the EGA
// black/white and the DAC's final eight entries are all black, leaving 246
// usable colors of the hardware 256.
constexpr auto kVga256 = [] {
  std::array<RgbColor, 246> colors{};
  std::size_t index = 0;
  const auto dac = [](std::uint8_t value) {
    return static_cast<std::uint8_t>((value << 2) | (value >> 4));
  };
  const auto push = [&](std::uint8_t red, std::uint8_t green, std::uint8_t blue) {
    colors[index++] = RgbColor{dac(red), dac(green), dac(blue)};
  };

  constexpr std::array<std::array<std::uint8_t, 3>, 16> ega = {{
      {0x00, 0x00, 0x00}, {0x00, 0x00, 0x2A}, {0x00, 0x2A, 0x00}, {0x00, 0x2A, 0x2A},
      {0x2A, 0x00, 0x00}, {0x2A, 0x00, 0x2A}, {0x2A, 0x15, 0x00}, {0x2A, 0x2A, 0x2A},
      {0x15, 0x15, 0x15}, {0x15, 0x15, 0x3F}, {0x15, 0x3F, 0x15}, {0x15, 0x3F, 0x3F},
      {0x3F, 0x15, 0x15}, {0x3F, 0x15, 0x3F}, {0x3F, 0x3F, 0x15}, {0x3F, 0x3F, 0x3F},
  }};
  for (const auto& color : ega) {
    push(color[0], color[1], color[2]);
  }

  // Gray ramp without its endpoints (they duplicate EGA black and white).
  constexpr std::array<std::uint8_t, 14> grays = {0x05, 0x08, 0x0B, 0x0E, 0x11, 0x14, 0x18,
                                                  0x1C, 0x20, 0x24, 0x28, 0x2D, 0x32, 0x38};
  for (const auto gray : grays) {
    push(gray, gray, gray);
  }

  // Nine 5-value ramps {v0, v1, v2, v3, max}: three brightness tiers (max 0x3F /
  // 0x1C / 0x10) times three saturation levels. Each drives one 24-hue wheel.
  constexpr std::array<std::array<std::uint8_t, 5>, 9> ramps = {{
      {0x00, 0x10, 0x1F, 0x2F, 0x3F}, {0x1F, 0x27, 0x2F, 0x37, 0x3F}, {0x2D, 0x31, 0x36, 0x3A, 0x3F},
      {0x00, 0x07, 0x0E, 0x15, 0x1C}, {0x0E, 0x11, 0x15, 0x18, 0x1C}, {0x14, 0x16, 0x18, 0x1A, 0x1C},
      {0x00, 0x04, 0x08, 0x0C, 0x10}, {0x08, 0x0A, 0x0C, 0x0E, 0x10}, {0x0B, 0x0C, 0x0D, 0x0F, 0x10},
  }};
  // The BIOS hue enumeration: blue -> magenta -> red -> yellow -> green -> cyan,
  // four steps per sextant; entries are ramp indices for (R, G, B).
  constexpr std::array<std::array<std::uint8_t, 3>, 24> wheel = {{
      {0, 0, 4}, {1, 0, 4}, {2, 0, 4}, {3, 0, 4}, {4, 0, 4}, {4, 0, 3}, {4, 0, 2}, {4, 0, 1},
      {4, 0, 0}, {4, 1, 0}, {4, 2, 0}, {4, 3, 0}, {4, 4, 0}, {3, 4, 0}, {2, 4, 0}, {1, 4, 0},
      {0, 4, 0}, {0, 4, 1}, {0, 4, 2}, {0, 4, 3}, {0, 4, 4}, {0, 3, 4}, {0, 2, 4}, {0, 1, 4},
  }};
  for (const auto& ramp : ramps) {
    for (const auto& hue : wheel) {
      push(ramp[hue[0]], ramp[hue[1]], ramp[hue[2]]);
    }
  }
  return colors;
}();

