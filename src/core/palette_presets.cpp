// Amstrad CPC: 3 levels per channel = 27 colors, enumerated red-major with levels
// 00/80/FF (the firmware ink numbering is a different order; this listing is by
// component for readability).
constexpr auto kAmstradCpc = [] {
  std::array<RgbColor, 27> colors{};
  constexpr std::array<std::uint8_t, 3> levels{0x00, 0x80, 0xFF};
  std::size_t index = 0;
  for (const auto red : levels) {
    for (const auto green : levels) {
      for (const auto blue : levels) {
        colors[index++] = RgbColor{red, green, blue};
      }
    }
  }
  return colors;
}();

