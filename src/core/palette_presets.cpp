// The full EGA 64-color space: every 2-bit-per-channel combination, enumerated
// red-major with levels 00/55/AA/FF (the hardware has no canonical listing order).
constexpr auto kEga64 = [] {
  std::array<RgbColor, 64> colors{};
  constexpr std::array<std::uint8_t, 4> levels{0x00, 0x55, 0xAA, 0xFF};
  std::size_t index = 0;
  for (const auto red : levels) {
    for (const auto green : levels) {
      for (const auto blue : levels) {
        colors[index++] = RgbColor{red, green, blue};
      }
    }
  }
  return colors;
}();

