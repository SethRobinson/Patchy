std::span<const PalettePreset> builtin_palette_presets() noexcept {
  return kPresets;
}

const PalettePreset* find_builtin_palette_preset(std::string_view id) noexcept {
  for (const auto& preset : kPresets) {
    if (id == preset.id) {
      return &preset;
    }
  }
  return nullptr;
}

